using System;
using NUnit.Framework;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace EvalTask
{
    [TestFixture]
    class EvalProgram_Should
    {
        private EvalProgram evalProgram;

        [SetUp]
        public void SetUp()
        {
            evalProgram = new EvalProgram();
        }

        [TestCase("2.2 + 4", Result = 6.2)]
        [TestCase("0 + 0", Result = 0)]
        [TestCase("-1.5 + -4.8", Result = -5.13)]
        [TestCase("-6.13 + 12.1", Result = 5.88)]
        public void Sum_TwoNums(string expression)
        {
            evalProgram.SolveExpression(expression);
        }

        [TestCase("2 - 2", Result = 0)]
        [TestCase("2 - (-2.2)", Result = 4.2)]
        [TestCase("0 - 0", Result = 0)]
        [TestCase("-6.3 - 2", Result = -8.3)]
        public void Deduct_TwoNums(string expression)
        {
            evalProgram.SolveExpression(expression);
        }
    }
}
